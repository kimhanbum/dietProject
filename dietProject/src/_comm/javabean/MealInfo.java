package _comm.javabean;


public class MealInfo {
	private String meal_code;
	private String meal_name;
	private int meal_carb;
	private int meal_fat;
	private int meal_protein;
	private int meal_cal;
	
	public String getMeal_code() {
		return meal_code;
	}

	public void setMeal_code(String meal_code) {
		this.meal_code = meal_code;
	}

	public String getMeal_name() {
		return meal_name;
	}

	public void setMeal_name(String meal_name) {
		this.meal_name = meal_name;
	}

	public int getMeal_carb() {
		return meal_carb;
	}

	public void setMeal_carb(int meal_carb) {
		this.meal_carb = meal_carb;
	}

	public int getMeal_fat() {
		return meal_fat;
	}

	public void setMeal_fat(int meal_fat) {
		this.meal_fat = meal_fat;
	}

	public int getMeal_protein() {
		return meal_protein;
	}

	public void setMeal_protein(int meal_protein) {
		this.meal_protein = meal_protein;
	}

	public int getMeal_cal() {
		return meal_cal;
	}

	public void setMeal_cal(int meal_cal) {
		this.meal_cal = meal_cal;
	}

	public MealInfo() {
		
	}
}
