package _comm.javabean;


public class DietReply {
	private String id;
	private String diet_code;
	private int diet_re_seq;
	private String diet_re_content;
	private String diet_re_date;
	
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getDiet_code() {
		return diet_code;
	}

	public void setDiet_code(String diet_code) {
		this.diet_code = diet_code;
	}

	public int getDiet_re_seq() {
		return diet_re_seq;
	}

	public void setDiet_re_seq(int diet_re_seq) {
		this.diet_re_seq = diet_re_seq;
	}

	public String getDiet_re_content() {
		return diet_re_content;
	}

	public void setDiet_re_content(String diet_re_content) {
		this.diet_re_content = diet_re_content;
	}

	public String getDiet_re_date() {
		return diet_re_date;
	}

	public void setDiet_re_date(String diet_re_date) {
		this.diet_re_date = diet_re_date;
	}

	public DietReply() {
		
	}
}
