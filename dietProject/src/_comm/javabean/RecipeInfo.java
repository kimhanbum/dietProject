package _comm.javabean;


public class RecipeInfo {
	private String id;
	private String recipe_code;
	private int recipe_num;
	private String recipe_subject;
	private String recipe_content;
	private String recipe_summary;
	private String recipe_file;
	private String recipe_date;
	
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getRecipe_code() {
		return recipe_code;
	}

	public void setRecipe_code(String recipe_code) {
		this.recipe_code = recipe_code;
	}

	public int getRecipe_num() {
		return recipe_num;
	}

	public void setRecipe_num(int recipe_num) {
		this.recipe_num = recipe_num;
	}

	public String getRecipe_subject() {
		return recipe_subject;
	}

	public void setRecipe_subject(String recipe_subject) {
		this.recipe_subject = recipe_subject;
	}

	public String getRecipe_content() {
		return recipe_content;
	}

	public void setRecipe_content(String recipe_content) {
		this.recipe_content = recipe_content;
	}

	public String getRecipe_summary() {
		return recipe_summary;
	}

	public void setRecipe_summary(String recipe_summary) {
		this.recipe_summary = recipe_summary;
	}

	public String getRecipe_file() {
		return recipe_file;
	}

	public void setRecipe_file(String recipe_file) {
		this.recipe_file = recipe_file;
	}

	public String getRecipe_date() {
		return recipe_date;
	}

	public void setRecipe_date(String recipe_date) {
		this.recipe_date = recipe_date;
	}

	public RecipeInfo() {
		
	}
}
