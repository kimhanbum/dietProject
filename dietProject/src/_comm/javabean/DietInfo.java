package _comm.javabean;

public class DietInfo {
	private String id;
	private String diet_code;
	private String diet_name;
	private String diet_form;
	private boolean diet_share;
	private int diet_recomm;
	private int diet_total_carb;
	private int diet_total_fat;
	private int diet_total_protein;
	private int diet_total_cal;
	private String diet_date;
	
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getDiet_code() {
		return diet_code;
	}

	public void setDiet_code(String diet_code) {
		this.diet_code = diet_code;
	}

	public String getDiet_name() {
		return diet_name;
	}

	public void setDiet_name(String diet_name) {
		this.diet_name = diet_name;
	}

	public String getDiet_form() {
		return diet_form;
	}

	public void setDiet_form(String diet_form) {
		this.diet_form = diet_form;
	}

	public boolean isDiet_share() {
		return diet_share;
	}

	public void setDiet_share(boolean diet_share) {
		this.diet_share = diet_share;
	}

	public int getDiet_recomm() {
		return diet_recomm;
	}

	public void setDiet_recomm(int diet_recomm) {
		this.diet_recomm = diet_recomm;
	}

	public int getDiet_total_carb() {
		return diet_total_carb;
	}

	public void setDiet_total_carb(int diet_total_carb) {
		this.diet_total_carb = diet_total_carb;
	}

	public int getDiet_total_fat() {
		return diet_total_fat;
	}

	public void setDiet_total_fat(int diet_total_fat) {
		this.diet_total_fat = diet_total_fat;
	}

	public int getDiet_total_protein() {
		return diet_total_protein;
	}

	public void setDiet_total_protein(int diet_total_protein) {
		this.diet_total_protein = diet_total_protein;
	}

	public int getDiet_total_cal() {
		return diet_total_cal;
	}

	public void setDiet_total_cal(int diet_total_cal) {
		this.diet_total_cal = diet_total_cal;
	}

	public String getDiet_date() {
		return diet_date;
	}

	public void setDiet_date(String diet_date) {
		this.diet_date = diet_date;
	}

	public DietInfo() {
		
	}
}
