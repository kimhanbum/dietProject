package _comm.javabean;


public class PersonalInfo {
	private String id;
	private int goal;
	private int hegiht;
	private int weight;
	private int goa_weight;
	private String rmr;
	
	public String getId() {
		return id;
	}


	public void setId(String id) {
		this.id = id;
	}


	public int getGoal() {
		return goal;
	}


	public void setGoal(int goal) {
		this.goal = goal;
	}


	public int getHegiht() {
		return hegiht;
	}


	public void setHegiht(int hegiht) {
		this.hegiht = hegiht;
	}


	public int getWeight() {
		return weight;
	}


	public void setWeight(int weight) {
		this.weight = weight;
	}


	public int getGoa_weight() {
		return goa_weight;
	}


	public void setGoa_weight(int goa_weight) {
		this.goa_weight = goa_weight;
	}


	public String getRmr() {
		return rmr;
	}


	public void setRmr(String rmr) {
		this.rmr = rmr;
	}


	public PersonalInfo() {
		
	}
}
