package _comm.javabean;


public class FoodInfo {
	private int food_code;
	private String food_name;
	private int food_carb; 
	private int food_fat;
	private int food_protein;
	private int food_cal;
	private int food_type;
	private int nutrient_type;
	
	public int getFood_code() {
		return food_code;
	}

	public void setFood_code(int food_code) {
		this.food_code = food_code;
	}

	public String getFood_name() {
		return food_name;
	}

	public void setFood_name(String food_name) {
		this.food_name = food_name;
	}

	public int getFood_carb() {
		return food_carb;
	}

	public void setFood_carb(int food_carb) {
		this.food_carb = food_carb;
	}

	public int getFood_fat() {
		return food_fat;
	}

	public void setFood_fat(int food_fat) {
		this.food_fat = food_fat;
	}

	public int getFood_protein() {
		return food_protein;
	}

	public void setFood_protein(int food_protein) {
		this.food_protein = food_protein;
	}

	public int getFood_cal() {
		return food_cal;
	}

	public void setFood_cal(int food_cal) {
		this.food_cal = food_cal;
	}

	public int getFood_type() {
		return food_type;
	}

	public void setFood_type(int food_type) {
		this.food_type = food_type;
	}

	public int getNutrient_type() {
		return nutrient_type;
	}

	public void setNutrient_type(int nutrient_type) {
		this.nutrient_type = nutrient_type;
	}

	public FoodInfo() {
		
	}
}
